package org.gearvrf.arpet.util;

import android.content.Context;
import android.content.pm.ApplicationInfo;
import android.content.pm.PackageManager;
import android.os.Bundle;
import android.support.annotation.NonNull;
import android.util.Log;

public final class ContextUtils {

    private static final String TAG = ContextUtils.class.getSimpleName();

    private ContextUtils() {
    }

    public static boolean isMetaDataSet(@NonNull Context context, @NonNull String metaDataName) {
        String value = ContextUtils.getMetaDataValue(context, metaDataName);
        return value != null && value.length() > 0;
    }

    public static String getMetaDataValue(@NonNull Context context, @NonNull String metaDataName) {
        try {
            ApplicationInfo ai = context.getPackageManager().getApplicationInfo(context.getPackageName(), PackageManager.GET_META_DATA);
            Bundle bundle = ai.metaData;
            return bundle.getString(metaDataName);
        } catch (PackageManager.NameNotFoundException e) {
            Log.e(TAG, "Failed to load meta-data, NameNotFound: " + e.getMessage());
        } catch (NullPointerException e) {
            Log.e(TAG, "Failed to load meta-data, NullPointer: " + e.getMessage());
        }
        return null;
    }

}
